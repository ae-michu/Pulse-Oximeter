// draw BPM value on screen
void Display :: drawBPMValue(int value) {
    setFont(u8g2_font_profont22_mn);           //set font
    char strValue[3];                          
    itoa(value, strValue, 10);                 // convert data from integer to array

    int numberOfDigits = 0;                    // calculate number of digits 
    while (value != 0) {
        value = value / 10;
        numberOfDigits++;
    }

    // display centered value on screen (based on number of digits shift drawn value to left / right)
    if (numberOfDigits == 1 || numberOfDigits == 0) {
        drawStr(94, 35, strValue);
    } else if (numberOfDigits == 2) {
        drawStr(88, 35, strValue);
    } else if (numberOfDigits == 3) {
        drawStr(82, 35, strValue);
    } else {
        drawStr(94, 35, "999");
    }
}

